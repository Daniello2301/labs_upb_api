package co.edu.upb.labs_upb.model;

/**
 * The TokenType enum is used to define the types of tokens that can be used in the application.
 * Currently, it only supports BEARER tokens.
 */
public enum TokenType {
    /**
     * Represents a Bearer token in the OAuth 2.0 authorization scheme.
     * Bearer tokens are the predominant type of access token used with OAuth 2.0.
     */
    BEARER
}
